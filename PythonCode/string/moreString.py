# slice Stirng
str1 = 'Hello world!'
print('Chuoi con tu phan tu thu 0 den phan tu thu 7:', str1[:8])

# modify string 
# upcase, lowcase 
print('Chuoi str1 sau khi upcase:', str1.upper())
print('Chuoi str1 su khi lowcase:', str1.lower())

# remove whitespace 
str2 = ' pham '
print('Chuoi str2 sau khi duoc xoa khoang trang o dau va cuoi:', str2.strip())

# split string, tham so dau vao la ki tu can tach
str3 = 'Pham#Khac#Duy'
print('Chuoi sau khi duoc split:', str3.split('#'))

